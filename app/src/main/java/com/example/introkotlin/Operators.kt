package com.example.introkotlin

fun main() {

    // Arithmetic Operators
    var x: Float = 6f
    var y: Float = 7f

    println(x + y)
    println(x - y)
    println(x * y)
    println(x / y)
    println(x % y)

    // Increment Operator
    x++

    // Decrement Operator
    x--

    println("X value: $x")

}